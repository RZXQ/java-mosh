package part_01._02_Types._07_Escape_Sequences;

public class _07_Escape_Sequences {

	public static void main(String[] args) {

		/*
		 * ===============================================================================
		 * Double Quote (\")
		 * ===============================================================================
		 */
		System.out.println("\"");

		/*
		 * ===============================================================================
		 * Backslash (\\)
		 * ===============================================================================
		 */
		System.out.println("\\");

		/*
		 * ===============================================================================
		 * Newline (\n)
		 * ===============================================================================
		 */
		System.out.println("\n");

		/*
		 * ===============================================================================
		 * Tab (\t)
		 * ===============================================================================
		 */
		System.out.println("\t");
	}

}
